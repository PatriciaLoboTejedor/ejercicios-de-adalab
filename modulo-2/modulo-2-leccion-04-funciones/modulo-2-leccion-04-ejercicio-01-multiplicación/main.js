"use strict";

function mult(a, b) {
  return a * b;
}

// CONSOLEANDO EL RESULTADO DIRECTAMENTE
console.log(mult(2, 56));
//CREANDO UNA VARIABLE EN BASE AL RESULTADO PARA LUEGO CONSOLEARLA
const amount1 = mult(2, 56);
console.log(amount1);

const amount2 = mult(25, 56);
console.log(amount2);

const amount3 = mult(41, 256);
console.log(amount3);
